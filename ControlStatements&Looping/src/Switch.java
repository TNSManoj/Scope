
public class Switch {

	public static void main(String[] args) {
	        int i=0;
	        switch(i) {
	        case 0:
	        	System.out.println("it is 0");
	        	break;
	        case 1:
	        	System.out.println("it is 1");
	        	case 2:
	        		System.out.println("it is 2");
	        	case 3:
	        		System.out.println("it is 3");
	        		break;
	        	case 4:
	        		System.out.println("default");
	        	}
	        
	        	
	        	
	    	 
	     
        
	}

}
