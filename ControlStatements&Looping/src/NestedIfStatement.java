
public class NestedIfStatement {

	public static void main(String[] args) {
		int i=3;
		if(i==0) {
			System.out.println("zero");
		}
		else if(i==1) {
			System.out.println("one");
		}
		else if(i==2) {
			System.out.println("two");
		}
		else if(i==3) {
			System.out.println("three");
		}
		else {
			System.out.println("default");
		}
	}

}
